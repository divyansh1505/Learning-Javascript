a = +prompt("What is the day today? (1-7) : ")

switch(a){
    case a=1:
        alert("Yes, It is Sunday");
        break;
    case a=2:
        alert("Yes, It is Monday");
        break;
    case a=3:
        alert("Yes, It is Tuesday");
        break;
    case a=4:
        alert("Yes, It is Wednesday");
        break;  
    case a=5:
        alert("Yes, It is Thursday");
        break;  
    case a=6:
        alert("Yes, It is Friday");
        break;  
    case a=7:
        alert("Yes, It is Saturday");
        break;
    default:
        alert("Invalid response");
}